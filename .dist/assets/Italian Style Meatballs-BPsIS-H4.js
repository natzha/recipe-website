const e=`# Italian Style Meatballs\r
\r
## Notes\r
Makes like 50 meatballs. I would recommend halving this recipe unless you are bulk making to freeze. I would cook with the sauce then freeze it all together.  \r
\r
## Ingredients\r
### Main\r
Ground Beef,  2kg  \r
Onion,  1 large or 2 medium  \r
Milk,  1/2 Cup  \r
Bread Crumbs,  200g  \r
Egg,  3  \r
Italian Herb Mix,  vibes  \r
Chilli Powder,  vibes  \r
Salt,  vibes  \r
Pepper,  vibes  \r
MSG,  vibes  \r
Garlic,  1 head  \r
Olive Oil,  a splash  \r
Tomato Paste, 400g  \r
Cheap White Wine,  1 Cup  \r
Crushed Tomato,  800g  \r
\r
### Other\r
\r
## Method\r
1. Blitz the onion (quatered) and the milk in a food processor or blender until slightly chunky but pretty smooth.  \r
2. In a large mixing bowl, pour in breadcrumbs then onion milk mixture. Stir til combined.  \r
3. Mix in ground beef, eggs, seasonings (Italian herb mix through to MSG in ingredients list). Don't overmix or else the mixture will turn into sausage but make sure there are no big clumps of bread.  \r
4. Shape mixture into rough balls and toss between hands to shape into meatballs. Can use an icecream scoop or similar to portion it out.  \r
5. Bake at highest oven setting (mine was 260 fan forced) until outside is well caramelised. A LOT of oil will come out so no need to brush with oil. Can opt to fry the meatballs if you want tastier but also higher calore.  \r
6. Make the sauce by mincing garlic and quickly sauteing in olive oil before adding tomato paste.  \r
7. Cook out tomato paste then add white wine.  \r
8. Cook out white win to evaporate alcohol then add crushed tomato.  \r
9. Season to taste with salt, pepper, herbs of choice (Basil would be good here, otherwise italian herb mix).  \r
10. Pour in meatballs and simmer on low for as long as you like, I would say at least 30 mins. Be careful when stirring to not break up the meatballs.  \r
11. Serve as desired.  `;export{e as default};

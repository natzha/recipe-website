const n=`# Purin\r
\r
## Notes\r
Makes 4 in the green bowl  \r
\r
## Ingredients\r
### Main\r
White Sugar,  75g  \r
Water,  3 Tbsp  \r
Eggs,  3 large  \r
Milk,  400ml  \r
White Sugar,  50g  \r
### Other\r
Softened Butter/Oil  \r
Matcha Powder or other flavours (optional)  \r
## Method\r
1. Cook 75g sugar and 2 Tbsp water in a non-stick saucepan until dark brown.  \r
2. Add in 1 Tbsp of boiling water to prevent overcooking and thin the caramel so its easier to pour.  \r
3. Pour out evenly into 4 small bowls. Pre-warm the bowls so the caramel stays liquid and has time to pool into a nice layer.  \r
4. Warm milk in the microwave and beat eggs and sugar in another bowl.  \r
5. Combine milk, eggs and sugar and strain into a measuring cup/bowl/container with a pouring spout.  \r
6. Pour mix out evenly among 4 greased bowls (using either butter or oil).  \r
7. Cover TIGHTLY with aluminium foil. Make it as airtight as possible so that steam doesn't get in.  \r
8. Steam for 20 then turn off the fire and steam in residual heat for 10 mins.  \r
9. Take off al-foil and place a plate ontop, invert and tap tap tap to get the pudding onto the plate.  \r
10. Eat and enjoy.  `;export{n as default};

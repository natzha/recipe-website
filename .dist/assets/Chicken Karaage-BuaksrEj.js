const e=`# Chicken Karaage\r
\r
## Notes\r
Makes enough for about 3 Cary servings\r
\r
## Ingredients\r
### Main\r
Chicken Thigh (skin on best),  1000-1200 grams  \r
Salt,  1 tsp  \r
Grated Ginger,  1 tsp  \r
Garlic,  2 cloves miced/grated  \r
Soy Sauce,  1 Tbsp  \r
Cooking Sake,  1 Tbsp  \r
Potato Starch,  prepare around 2 Cups (may not need all of it)  \r
Neutral Frying Oil (vegetable, canola, sunflower), 1 Litre  \r
\r
### Other\r
Black Pepper  \r
Japanese Mayo  \r
Japanese Chilli Powder  \r
\r
## Method\r
1. Cut chicken into slightly larger than bite size pieces. They will shrink when they cook so cut them bigger.  \r
2. Put into a large bowl along with salt, ginger, garlic, soy sauce, cooking sake. Crack in black pepper to taste (go hard on the pepper).  \r
3. Marinate for at least 20 mins,  preferably for a few hours or even overnight.  \r
4. Start heating up your frying oil. We are double frying so first fry doesn't have to be too hot. Soak a wooden chopstick in water and it should bubble lightly when it is put in the oil.  \r
5. Prepare a large tray/plate and bowl. Fill bowl with 1 cup potato starch.  \r
6. Working with about 4 pieces at a time, let the chicken drip excess liquid back into the marinating bowl and put the chicken in the potato starch bowl and coat.  \r
7. Spread the chicken out in 1 layer on the tray.  \r
8. Let chicken sit for 5 mins, potato starch will draw liquid off the chicken and air dry.  \r
9. Dust the chicken with potato starch again to coat any areas that are a bit wet/have no coating.  \r
10. Place a wire rack over a tray lined with paper towels. This is the drip tray so its gonna get oily.  \r
11. Fry about 8 pieces of chicken at a time for about 5 until cornstarch is set and just starting to colour. You should notice it start to bubble heavily as inside starts to cook and water evaporates out.  \r
12. Let it drip over the wire rack and repeat the frying for the rest of the chicken.  \r
13. Heat the oil up until a soaked chopstick bubbles heavily when its put in the oil.  \r
14. Re-fry the chicken for about 3 minutes until the outside is a golden brown.  \r
15. Season with japanese chilli powder and serve with japanese mayo.  `;export{e as default};

const n=`# Peanut Butter\r
\r
## Notes\r
\r
\r
## Ingredients\r
### Main\r
Raw Peanuts,    2.5 times container volume\r
\r
### Other\r
Salt\r
\r
## Method\r
1. Roast peanuts in an oven at 200C until browned\r
2. Let cool then add to food processor\r
3. Blitz on low speed, scraping regularly until smooth\r
4. Remove blades and scrape the bottom to pick up any chunks under the blades, transfer to a bowl.\r
5. Add blades and peanut butter back in with a pinch of salt then blitz until combined.\r
6. Transfer to container\r
`;export{n as default};
